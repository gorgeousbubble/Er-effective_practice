-module(mylists).

%% ====================================================================
%% API functions
%% ====================================================================
-export([sum/1]).
-export([map/2]).


%% ====================================================================
%% Internal functions
%% ====================================================================

%% sum function
sum([])	->
	0;
sum([H|T])	->
	H + sum(T).

%% map function
map(_, [])	->
	[];
map(F, [H|T])	->
	[F(H)|map(F, T)].